// Rust use .rs as extension

// The main function is the entry point of every Rust program
fn main() {
    // println! is a macro in Rust used for printing text to the console
    // "Hello World this is ShellBng!!!" is the text to be printed
    println!("Hello World this is ShellBng!!!");
}


// println! calls a Rust macro. If it had called a 
//function instead, it would be entered as println (without the !)

// ! means that you’re calling a macro instead of a normal function and that 
// macros don’t always follow the same rules as functions.

// If you’re more familiar with a dynamic language,
// such as Ruby, Python, or JavaScript, you might not be used to compiling and running a program as separate steps.
// Rust is an ahead-of-time compiled language, meaning you can compile a program and give the executable to someone else,
// and they can run it even without having Rust installed. If you give someone a .rb, .py, or .js file, they need to have a Ruby, Python, 
// or JavaScript implementation installed (respectively). But in those languages, you only need one command to compile and run your program.
// Everything is a trade-off in language design.

// To make executable 

// rustc main.rs
//To run in windows .\index.exe in linux ./index.exe
